    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            //creating new socket
            this.socket = new Socket("127.0.0.1", 6002);
            //br i pw for reading and sending messages to server
            this.br = new BufferedReader(new InputStreamReader(this.socket.getInputStream()));
            this.pw = new PrintWriter(new OutputStreamWriter(this.socket.getOutputStream()), true);
            //creating thread for each client
            //this refers to ClientDesign for further updates
            this.rmfs = new ReceiveMessageFromServer(this);
            Thread thr = new Thread(rmfs);
            thr.start();
            
            String porukaZaSlanje;
            if(!this.tfUsername.getText().equals("") && !this.tfPassword.getText().equals("")){
                if('*' == this.tfPassword.getText().charAt(0)){
                //username:password:role
                    porukaZaSlanje = this.tfUsername.getText() + ":" + this.pass + ":" + this.cbRole.getSelectedItem(); 
                }else{
                    porukaZaSlanje = this.tfUsername.getText() + ":" + this.tfPassword.getText() + ":" + this.cbRole.getSelectedItem(); 
                }
                this.pw.println(porukaZaSlanje);
            } else {
                JOptionPane.showMessageDialog(null, "Wrong input");
            }

        } catch (IOException ex) {
            Logger.getLogger(ClientDesign.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed


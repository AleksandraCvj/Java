    private void cbHideItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbHideItemStateChanged
        if(isChecked){
            this.pass = this.tfPassword.getText();
            String hiddenPass = "";
            for( int i = 0; i < this.tfPassword.getText().length(); i++){
                hiddenPass = hiddenPass + "*";
            }
            this.tfPassword.setText(hiddenPass);
            this.isChecked = false;
        }else{
            this.tfPassword.setText(this.pass);
            this.isChecked = true;
        }
        
        
    }//GEN-LAST:event_cbHideItemStateChanged

